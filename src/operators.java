public class operators {
    public static void main(String[] args) {
        //Operators: operators are symbol that perform operations on data, such as number or variables, in programming languages like Java.

        /*
         Operators are of five types:
                                     1. Arithmetic operator---> (+,-,/,*,)
                                     2. Relational operator---> (<,>,<=,>=,!=,==)
                                     3. Logical operator---> (&&,||,!)
                                     4. Increment & Decrement operators---> (++Exp,Exp++), (--Exp,Exp--)
                                     5. Assignment operator are of two types Simple and Compound
                                     Simple---> (=), Compound---> (+=,-=,*=,/=)
         */
        int num1 = 20;
        int num2 = 30;

        num1+=10;
        System.out.println(num1);

        int sum = num1 + num2;
        System.out.println(sum);

    }
}
